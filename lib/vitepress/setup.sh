    "docs:setup": "../.vitepress-config/setup.sh"
  },
  "devDependencies": {
    "vitepress": "^1.4.5",
    "typescript": "^5.0.0",
    "vue": "^3.4.0"
  }
}
EOF

echo "   âœ… package.json updated with TypeScript support"

